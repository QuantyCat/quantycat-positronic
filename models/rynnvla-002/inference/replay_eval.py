#!/usr/bin/env python3
"""
Replay evaluator for RynnVLA-002 on saved training/validation samples.

Loads conversation samples generated by preprocessing, reconstructs the same
front/wrist/history/state inputs used during training, runs the current solver,
and compares predicted action chunks against ground-truth saved actions.
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import random
import sys
from pathlib import Path
from typing import Any

import numpy as np
from PIL import Image
import yaml

_DEFAULT_CONFIG = "models/rynnvla-002/config.yaml"


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[3]


def _load_positronic_config(config_path: Path) -> dict[str, Any]:
    if not config_path.is_file():
        raise FileNotFoundError(f"config not found: {config_path}")
    with open(config_path) as f:
        return yaml.safe_load(f)


def _resolve_checkpoint(args: argparse.Namespace, cfg: dict[str, Any], root: Path) -> Path:
    if args.checkpoint:
        return Path(args.checkpoint).expanduser().resolve()
    ckpt = cfg.get("checkpoint")
    if not ckpt:
        raise ValueError("Pass --checkpoint PATH or set checkpoint in config.yaml")
    return Path(ckpt).expanduser().resolve()


def _ensure_rynnvla_on_path(extra: str | None) -> None:
    if extra:
        p = Path(extra).expanduser().resolve()
        if p.is_dir():
            sys.path.insert(0, str(p))


def _load_image(path: str) -> np.ndarray:
    return np.asarray(Image.open(path).convert("RGB"), dtype=np.uint8)


def _load_state(path: str) -> np.ndarray:
    return np.asarray(np.load(path), dtype=np.float32)


def _load_action_chunk(paths: list[str]) -> np.ndarray:
    return np.asarray([np.load(p).astype(np.float32) for p in paths], dtype=np.float32)


def _resolve_conversation_file(args: argparse.Namespace, cfg: dict[str, Any], root: Path) -> Path:
    if args.conversation_file:
        return Path(args.conversation_file).expanduser().resolve()
    work_dir = Path(cfg["work_dir"])
    if not work_dir.is_absolute():
        work_dir = (root / work_dir).resolve()
    label = cfg["task_label"]
    his = cfg["his"]
    resolution = cfg["resolution"]
    split = args.split
    name = f"libero_{label}_his_{his}_{split}_img_state_abs_ck_1_{resolution}.json"
    path = work_dir / "conversations" / name
    if not path.is_file():
        raise FileNotFoundError(f"conversation file not found: {path}")
    return path


def _build_solver(args: argparse.Namespace, cfg: dict[str, Any], root: Path):
    work_dir = Path(cfg["work_dir"])
    if not work_dir.is_absolute():
        work_dir = (root / work_dir).resolve()
    os.environ["RYNNVLA_ACTION_STATS_FILE"] = str((work_dir / "min_max_action.txt").resolve())
    os.environ["RYNNVLA_STATE_STATS_FILE"] = str((work_dir / "min_max_state.txt").resolve())

    ckpt_path = _resolve_checkpoint(args, cfg, root)
    _ensure_rynnvla_on_path(args.rynnvla_repo.strip() or None)

    try:
        from eval_solver_lerobot_action_head_state import Solver
    except ImportError as e:
        raise SystemExit(
            "Import failed: eval_solver_lerobot_action_head_state.Solver\n"
            "Add the QuantyCat RynnVLA-002 python root to PYTHONPATH, e.g.:\n"
            "  export PYTHONPATH=\"$HOME/RynnVLA-002/rynnvla-002:$PYTHONPATH\"\n"
            "Or pass --rynnvla-repo pointing at that directory.\n"
            f"Original error: {e}"
        ) from e

    import argparse as _argparse

    solver_args = _argparse.Namespace(
        resume_path=str(ckpt_path),
        output_dir=str(ckpt_path / "replay_eval_logs"),
        device=args.gpu if args.gpu is not None else cfg["gpu"],
        action_dim=cfg["action_dim"],
        time_horizon=cfg["chunk_size"],
        max_seq_len=cfg["max_seq_len"],
        mask_image_logits=cfg["mask_image_logits"],
        dropout=cfg["dropout"],
        z_loss_weight=cfg["inference_z_loss_weight"],
        his=cfg["his_mode"],
        action_steps=cfg["action_steps"],
        deterministic_crop=bool(args.deterministic_crop or cfg.get("deterministic_crop", False)),
    )

    Path(solver_args.output_dir).mkdir(parents=True, exist_ok=True)
    print(f"Loading Solver from {ckpt_path} …")
    return Solver(solver_args), ckpt_path


def _prepare_sample(conv: dict[str, Any], his: int):
    if len(conv["image"]) != his * 2:
        raise ValueError(f"expected {his*2} images, got {len(conv['image'])}")

    front_paths = conv["image"][:his]
    wrist_paths = conv["image"][his:]
    front_imgs = [_load_image(p) for p in front_paths]
    wrist_imgs = [_load_image(p) for p in wrist_paths]
    state = _load_state(conv["state"])
    gt_action = _load_action_chunk(conv["action"])

    prompt = conv["conversations"][0]["value"]
    prefix = "What action should the robot take to "
    if not prompt.startswith(prefix):
        raise ValueError(f"unexpected prompt format: {prompt[:80]!r}")
    task = prompt[len(prefix):].split("?", 1)[0].strip()

    return {
        "front_current": front_imgs[-1],
        "wrist_current": wrist_imgs[-1],
        "front_history": front_imgs[:-1],
        "wrist_history": wrist_imgs[:-1],
        "state": state,
        "gt_action": gt_action,
        "task": task,
        "front_paths": front_paths,
        "wrist_paths": wrist_paths,
        "action_paths": conv["action"],
        "state_path": conv["state"],
    }


def _reset_solver_history(solver, sample) -> None:
    solver.his_img = list(sample["front_history"])
    solver.his_wrist_img = list(sample["wrist_history"])


def _metrics(pred: np.ndarray, gt: np.ndarray) -> dict[str, float]:
    diff = np.asarray(pred, dtype=np.float32) - np.asarray(gt, dtype=np.float32)
    return {
        "mean_abs": float(np.mean(np.abs(diff))),
        "max_abs": float(np.max(np.abs(diff))),
        "l2": float(np.linalg.norm(diff)),
        "first_step_mean_abs": float(np.mean(np.abs(diff[0]))),
        "gripper_mean_abs": float(np.mean(np.abs(diff[:, -1]))),
        "joint_mean_abs": float(np.mean(np.abs(diff[:, :-1]))),
    }


def main() -> None:
    root = _repo_root()
    os.chdir(root)

    parser = argparse.ArgumentParser(description="Replay evaluator for RynnVLA-002.")
    parser.add_argument("--checkpoint", type=str, default=None)
    parser.add_argument("--positronic-config", type=str, default=_DEFAULT_CONFIG)
    parser.add_argument("--conversation-file", type=str, default=None)
    parser.add_argument("--split", type=str, default="train", choices=["train", "val_ind"])
    parser.add_argument("--limit", type=int, default=100, help="Number of samples to evaluate.")
    parser.add_argument("--offset", type=int, default=0, help="Starting index in the conversation file.")
    parser.add_argument("--random-sample", action="store_true", help="Sample examples randomly instead of sequentially.")
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument("--deterministic-crop", action="store_true")
    parser.add_argument("--gpu", type=int, default=None)
    parser.add_argument(
        "--rynnvla-repo",
        type=str,
        default=os.environ.get("RYNNVLA_REPO", ""),
        help="If set, prepend this directory to sys.path (e.g. ~/RynnVLA-002/rynnvla-002)",
    )
    args = parser.parse_args()

    cfg_path = Path(args.positronic_config)
    if not cfg_path.is_absolute():
        cfg_path = (root / cfg_path).resolve()
    cfg = _load_positronic_config(cfg_path)

    conv_path = _resolve_conversation_file(args, cfg, root)
    with open(conv_path) as f:
        conversations = json.load(f)

    if not conversations:
        raise SystemExit(f"No samples found in {conv_path}")

    rng = random.Random(args.seed)
    indices = list(range(len(conversations)))
    if args.random_sample:
        rng.shuffle(indices)
    else:
        indices = indices[args.offset:]
    indices = indices[: args.limit]
    if not indices:
        raise SystemExit("No samples selected.")

    solver, ckpt_path = _build_solver(args, cfg, root)
    his = int(cfg["his"])

    output_dir = ckpt_path / "replay_eval_logs"
    output_dir.mkdir(parents=True, exist_ok=True)
    csv_path = output_dir / f"replay_eval_{args.split}.csv"

    rows = []
    summary_acc = {
        "mean_abs": [],
        "max_abs": [],
        "l2": [],
        "first_step_mean_abs": [],
        "gripper_mean_abs": [],
        "joint_mean_abs": [],
    }

    print(f"conversation_file={conv_path}")
    print(f"num_conversations={len(conversations)}")
    print(f"selected_samples={len(indices)}")

    for out_idx, sample_idx in enumerate(indices):
        conv = conversations[sample_idx]
        sample = _prepare_sample(conv, his=his)
        _reset_solver_history(solver, sample)
        pred = solver.get_action_wrist_action_head_state(
            front_image=sample["front_current"],
            wrist_image=sample["wrist_current"],
            state=sample["state"],
            prompt=sample["task"],
        )
        pred = np.asarray(pred, dtype=np.float32)
        gt = sample["gt_action"]

        if pred.shape != gt.shape:
            raise ValueError(f"pred shape {pred.shape} != gt shape {gt.shape} for sample {sample_idx}")

        m = _metrics(pred, gt)
        for k, v in m.items():
            summary_acc[k].append(v)

        row = {
            "sample_index": sample_idx,
            "task": sample["task"],
            "state_path": sample["state_path"],
            "front_current": sample["front_paths"][-1],
            "wrist_current": sample["wrist_paths"][-1],
            "mean_abs": m["mean_abs"],
            "max_abs": m["max_abs"],
            "l2": m["l2"],
            "first_step_mean_abs": m["first_step_mean_abs"],
            "gripper_mean_abs": m["gripper_mean_abs"],
            "joint_mean_abs": m["joint_mean_abs"],
            "gt_first": np.round(gt[0], 6).tolist(),
            "pred_first": np.round(pred[0], 6).tolist(),
        }
        rows.append(row)

        if out_idx < 10 or (out_idx + 1) % 25 == 0:
            print(
                f"[sample {sample_idx}] "
                f"mean_abs={m['mean_abs']:.6f} "
                f"first_step_mean_abs={m['first_step_mean_abs']:.6f} "
                f"gripper_mean_abs={m['gripper_mean_abs']:.6f}"
            )

    with open(csv_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "sample_index",
                "task",
                "state_path",
                "front_current",
                "wrist_current",
                "mean_abs",
                "max_abs",
                "l2",
                "first_step_mean_abs",
                "gripper_mean_abs",
                "joint_mean_abs",
                "gt_first",
                "pred_first",
            ],
        )
        writer.writeheader()
        writer.writerows(rows)

    print(f"\nSaved per-sample metrics to {csv_path}")
    print("\nSummary")
    for key, values in summary_acc.items():
        arr = np.asarray(values, dtype=np.float32)
        print(f"  {key}: mean={arr.mean():.6f}  median={np.median(arr):.6f}  p90={np.percentile(arr, 90):.6f}")


if __name__ == "__main__":
    main()
